create table users(
  user_id bigint,
  email varchar(255),
  username varchar_ignorecase(50) not null primary key,
  password varchar_ignorecase(50) not null,
  enabled boolean not null
);

create table characters (char_id bigint generated by default as identity,
  user_id bigint,
  agility integer,
  appearance integer,
  attack_bonus integer,
  brawl integer,
  char_name varchar(255),
  char_type varchar(255),
  charisma integer,
  conjuring integer,
  conjuring_bonus integer,
  constitution integer,
  damage_bonus integer,
  defence integer,
  defence_bonus integer,
  dexterity integer,
  health_points integer,
  health_points_max integer,
  intelligence integer,
  magictalent integer,
  range integer,
  rank varchar(255),
  res_bonus_body integer,
  res_bonus_mind integer,
  resistance integer,
  stamina_bonus integer,
  stamina_points integer,
  stamina_points_max integer,
  strength integer,
  willpower integer,
  primary key (char_id)
);